$(document).ready(function() {
    $("#buttonleft").mouseenter(function() {
        $("#buttonleft").fadeTo("fast", 0.5)});
    $("#buttonleft").mouseleave(function() {
        $("#buttonleft").fadeTo("fast", 1)
        });
    $("#buttonright").mouseenter(function() {
        $("#buttonright").fadeTo("fast", 0.5)});
    $("#buttonright").mouseleave(function() {
        $("#buttonright").fadeTo("fast", 1)
        });
    });

